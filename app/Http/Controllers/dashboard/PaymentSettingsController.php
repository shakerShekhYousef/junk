    public function edit($id)
    {
        $payment=DB::table('payment_setting')->where('id',$id)->first();
        return view('dashboard.payment.edit',compact('payment'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $payment=PaymentSetting::where('id',$id)->first();
        $payment->update([
            'merchant_id'=>$request->merchant_id,
            'working_key'=>$request->working_key,
            'access_code'=>$request->access_code
        ]);
        session()->flash('success', 'Updated');
        return redirect()->route('payment',$id);
    }

